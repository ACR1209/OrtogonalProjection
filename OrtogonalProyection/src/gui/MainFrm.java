    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        double[][] base = Parser.getVector(input.getText());
        if(Vector.allDimensionsEqual(base)){
            double[][] ans = Vector.projectionOfABase(base);
            JLabel[] labels = {w1Lbl, w2Lbl, jLabel6};

            for(int i = 0; i < ans.length; i++){
                StringBuilder build = new StringBuilder();
                build.append("(");
                for(int j = 0; j < ans[0].length; j++){
                    if(j != ans[0].length - 1){
                        build.append(String.format("%.2f, ", ans[i][j]));
                    } else{
                        build.append(String.format("%.2f", ans[i][j]));
                    }
                }
                build.append(")");

                labels[i].setText("w" + (i+1) + ": " + build.toString());
                
            }  
            
            
        } else{
            JOptionPane.showMessageDialog(null, "Las dimensiones de los vectores deben ser iguales.");
        }
    }//GEN-LAST:event_calculateActionPerformed

